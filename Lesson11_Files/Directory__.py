import os

# Створення каталогів у поточному каталозі:
os.mkdir("new_dir")

# Метод chdir() для зміни поточного каталогу.
# Метод chdir() приймає аргумент, який є назвою каталогу, який ви хочете зробити поточним
os.chdir("/home/newdir")

# Метод getcwd() відображає поточний робочий каталог
os.getcwd()

# Метод rmdir () видаляє каталог, який передається як аргумент у методі
# (Перед видаленням каталогу слід видалити весь його вміст)
os.rmdir('dir_name')

