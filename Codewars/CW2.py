# Якщо ми перерахуємо всі натуральні числа до 10, кратні 3 або 5, то отримаємо 3, 5, 6 і 9.
# Сума цих чисел дорівнює 23. Доопрацюйте розв'язок так,
# щоб він повертав суму всіх чисел, кратних 3 або 5, менших за введене число.
# Крім того, якщо число від'ємне, виведіть 0.
# Зауваження: Якщо число кратне і 3, і 5, порахуйте його лише один раз.

def solution(number):
    return sum(n for n in range(number) if n % 3 ==0 or n % 5 == 0)

s = solution(1000)
print(s)
